-- SQL DE REPARO: TABELA DE PROGRESSO (user_course_progress)
-- Este script garante que a tabela aceite os IDs numéricos que o frontend está enviando.

-- 1. Remover a tabela antiga (se necessário) ou ajustar a estrutura
-- Vamos garantir que os tipos batam com o que o frontend envia (BIGINT)

DROP TABLE IF EXISTS public.user_course_progress CASCADE;

CREATE TABLE public.user_course_progress (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT REFERENCES public.user_profiles(id) ON DELETE CASCADE,
    course_id BIGINT REFERENCES public.courses(id) ON DELETE CASCADE,
    lesson_id BIGINT REFERENCES public.lessons(id) ON DELETE CASCADE,
    completed BOOLEAN DEFAULT FALSE,
    completed_at TIMESTAMPTZ DEFAULT NOW(),
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW(),
    -- Garantir que não haja duplicidade (um registro por aula por usuário)
    UNIQUE(user_id, lesson_id)
);

-- 2. Habilitar RLS
ALTER TABLE public.user_course_progress ENABLE ROW LEVEL SECURITY;

-- 3. Criar políticas de acesso (Liberação total para teste inicial)
CREATE POLICY "Allow all for progress" ON public.user_course_progress FOR ALL USING (true) WITH CHECK (true);

-- 4. Garantir que as tabelas de cursos/aulas também permitam leitura
ALTER TABLE public.courses ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.course_modules ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.lessons ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "Enable read access for all" ON public.courses;
CREATE POLICY "Enable read access for all" ON public.courses FOR SELECT USING (true);

DROP POLICY IF EXISTS "Enable read access for all" ON public.course_modules;
CREATE POLICY "Enable read access for all" ON public.course_modules FOR SELECT USING (true);

DROP POLICY IF EXISTS "Enable read access for all" ON public.lessons;
CREATE POLICY "Enable read access for all" ON public.lessons FOR SELECT USING (true);
