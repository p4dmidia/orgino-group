-- customer_coupons
CREATE TABLE IF NOT EXISTS public.customer_coupons (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT, -- Se o cliente for um usuário logado
