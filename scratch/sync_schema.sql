-- company_purchases
CREATE TABLE IF NOT EXISTS public.company_purchases (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id BIGINT REFERENCES public.companies(id),
    cashier_id BIGINT REFERENCES public.company_cashiers(id),
    cashier_cpf TEXT NOT NULL,
    customer_coupon_id BIGINT REFERENCES public.customer_coupons(id),
    customer_coupon TEXT NOT NULL,
    purchase_value NUMERIC(10,2) NOT NULL,
    cashback_percentage NUMERIC(5,2) NOT NULL,
    cashback_generated NUMERIC(10,2) NOT NULL,
    purchase_date DATE DEFAULT CURRENT_DATE,
    purchase_time TIME DEFAULT CURRENT_TIME,
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- commission_distributions
CREATE TABLE IF NOT EXISTS public.commission_distributions (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    purchase_id BIGINT REFERENCES public.company_purchases(id),
